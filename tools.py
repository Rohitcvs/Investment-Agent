class StockDataTool(BaseTool):
    """Fetches historical stock data for a given ticker."""
    name: str = "Stock_Data_Fetcher"
    description: str = (
        "Fetches historical stock data for a given ticker. "
        "Input should be a stock ticker symbol (e.g., 'AAPL')."
    )

    def _run(self, ticker: str) -> pd.DataFrame:
        """Fetches the stock data."""
        stock = yf.Ticker(ticker)
        # Get historical market data
        hist = stock.history(period="1y")
        if hist.empty:
            return f"Error: Could not find data for ticker {ticker}."
        return hist.to_string()

class TechnicalIndicatorTool(BaseTool):
    """Calculates technical indicators for a given stock."""
    name: str = "Technical_Indicator_Calculator"
    description: str = (
        "Calculates various technical indicators for a stock. "
        "Input should be a stock ticker symbol (e.g., 'AAPL')."
    )

    def _run(self, ticker: str) -> str:
        """Calculates the technical indicators."""
        stock = yf.Ticker(ticker)
        hist = stock.history(period="1y")
        if hist.empty:
            return f"Error: Could not find data for ticker {ticker}."
        
        # Calculate indicators
        hist.ta.rsi(append=True)
        hist.ta.macd(append=True)
        hist.ta.sma(length=20, append=True)
        hist.ta.bbands(append=True)
        
        # Return the last few rows with indicators
        return hist.iloc[-5:].to_string()

class EconomicResearcherTool(BaseTool):
    """Performs internet searches for financial news and sentiment."""
    name: str = "Economic_Researcher"
    description: str = (
        "Performs internet searches for financial news, sentiment, and "
        "macroeconomic factors about a provided company. "
        "Input should be a targeted search query (e.g., 'Apple Inc. investor sentiment last 6 months'). "
        "**Crucially, you must focus on trusted sources published within the last 6-12 months.** Avoid wikis, forums, or blogs."
    )
    
    def _run(self, query: str) -> str:
        """Executes an internet search and returns a formatted string of results."""
        try:
            with DDGS() as ddgs:
                # Fetch the top 5 results for the query
                results = list(ddgs.text(query, max_results=5))
                if not results:
                    return "No results found."
                
                # Format the results into a single string
                formatted_results = []
                for i, result in enumerate(results):
                    formatted_results.append(
                        f"Result {i+1}:\n"
                        f"  Title: {result.get('title', 'N/A')}\n"
                        f"  Snippet: {result.get('body', 'N/A')}\n"
                        f"  URL: {result.get('href', 'N/A')}\n"
                    )
                return "\n".join(formatted_results)
        except Exception as e:
            return f"An error occurred during the search: {e}"

