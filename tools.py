class MemoryTool(BaseTool):
    """A tool to save and retrieve past financial reports."""
    name: str = "Memory_Tool"
    description: str = (
        "Used to save and retrieve past financial reports to provide long-term context. "
        "Use 'save' to store a new report and 'retrieve' to fetch past reports for a ticker."
    )
    _file_path: str = "report_memory.json"

    def _load_memory(self) -> dict:
        """Loads the memory from a local JSON file."""
        if not os.path.exists(self._file_path):
            return {}
        with open(self._file_path, "r") as f:
            return json.load(f)

    def _save_memory(self, memory: dict):
        """Saves the memory to a local JSON file."""
        with open(self._file_path, "w") as f:
            json.dump(memory, f, indent=2)

    def _run(self, action: str, ticker: Optional[str] = None, report: Optional[str] = None) -> str:
        """
        Saves or retrieves a report.
        - To save: action='save', ticker='TICKER', report='...'
        - To retrieve: action='retrieve', ticker='TICKER'
        """
        memory = self._load_memory()
        
        if action == "save":
            if not ticker or not report:
                return "Error: 'save' action requires a 'ticker' and a 'report'."
            if ticker not in memory:
                memory[ticker] = []
            memory[ticker].append(report)
            self._save_memory(memory)
            return f"Report for {ticker} saved successfully."
            
        elif action == "retrieve":
            if not ticker:
                return "Error: 'retrieve' action requires a 'ticker'."
            if ticker in memory and memory[ticker]:
                return "\n---\n".join(memory[ticker])
            return f"No past reports found for {ticker}."
            
        return "Error: Invalid action. Use 'save' or 'retrieve'."
